    <h2 class="set-bg">COLOR ON SCREEN V.S. COLOR WHEN PRINTED</h2>
        <p>4 Color Process printing or commonly called Full Color printing is made up of 4 colors, Cyan Magenta Yellow & K for Black. Computer screens represent full color using 3 colors Red Green Blue, plus or minus light. Because a computer screen can vary the brightness of the light behind those colors, RGB has a larger gamut (the entire range of colors available on a particular device such as a monitor or printer) than full color CMYK printing therefore some colors may appear brighter on screen or there may be a slight color shift to a darker shade when printed on press. If you are concerned about color we recommend you check colors with a Pantone® swatch book prior to printing. We print in 4 Color Process and the Pantone® swatch book will have 4 dots next to any color that can be accurately represented with CMYK printing. Any Pantone® colors other than that we will do our very best to get as close as possible. This color shift is most pronounced in spot colors and is why when designing logos or branding with a specific spot color we recommend verifying it is CMYK compatible first to ensure your printed results match your expectations. Our press is equipped with special densitometers that read the color value of every sheet and adjusts on the fly between sheets to ensure you receive the finest quality possible.</p>
   

